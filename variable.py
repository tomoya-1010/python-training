#pythonの変数は変数宣言や型の宣言は不要で、
#変数名と値をイコールで結べばそのまま変数として使用することができる。

# 変数を定義する
a = 1 # 数値
b = 'AAA' # 文字列
c = [1, 2, 3] # リスト
d = {'apple': 200, 'orange': 100, 'banana': 150}  # 辞書
 
# 定義した変数の内容を出力してみる
print(a) # 1
print(b) # AAA
print(c) # [1, 2, 3]
print(d) # {'orange': 100, 'banana': 150, 'apple': 200}
 
